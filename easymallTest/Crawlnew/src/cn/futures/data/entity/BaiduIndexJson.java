package cn.futures.data.entity;

import java.io.Serializable;

public class BaiduIndexJson implements Serializable{

	/**
	 * 百度指数返回值json转化
	 */
	private static final long serialVersionUID = 5598756611549480462L;
	private String error_code;
	private String reason;
	@Override
	public String toString() {
		return "BaiduIndexJson [error_code=" + error_code + ", reason=" + reason + ", data=" + data + "]";
	}
	public String getError_code() {
		return error_code;
	}
	public void setError_code(String error_code) {
		this.error_code = error_code;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public BaiduIndexJsonData getData() {
		return data;
	}
	public void setData(BaiduIndexJsonData data) {
		this.data = data;
	}
	public BaiduIndexJson(String error_code, String reason, BaiduIndexJsonData data) {
		super();
		this.error_code = error_code;
		this.reason = reason;
		this.data = data;
	}
	public BaiduIndexJson() {
		super();
		// TODO Auto-generated constructor stub
	}
	private BaiduIndexJsonData data;

	
}
